//1 - Criar um loop que conte de 1 até 10 usando FOR

console.log("1)-----");

for(let i = 1;i <= 10;i++){
  console.log(i);
}

//2 - Criar um loop que conte de 10 até 1 usando WHILE

console.log("2)-----");

let cont = 10;

while(cont >= 1){
  console.log(cont);
  cont --;
}

